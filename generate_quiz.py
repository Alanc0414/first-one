# ===================== Excel 工具函数 =====================

def thin_border():
    s = Side(style='thin', color=BORDER_CLR)
    return Border(left=s, right=s, top=s, bottom=s)


def set_header_row(ws, title, title_bg):
    ws.merge_cells('A1:G1')
    cell = ws['A1']
    cell.value = title
    cell.font = Font(name='微软雅黑', bold=True, size=14, color=GOLD)
    cell.fill = PatternFill('solid', fgColor=title_bg)
    cell.alignment = Alignment(horizontal='center', vertical='center')
    ws.row_dimensions[1].height = 38


def set_col_header_row(ws):
    hf = Font(name='微软雅黑', bold=True, size=11, color='3B1F00')
    hfill = PatternFill('solid', fgColor=HEADER_BG)
    ha = Alignment(horizontal='center', vertical='center', wrap_text=True)
    for col_idx, col_name in enumerate(COLUMNS, 1):
        cell = ws.cell(row=2, column=col_idx, value=col_name)
        cell.font = hf
        cell.fill = hfill
        cell.alignment = ha
        cell.border = thin_border()
    ws.row_dimensions[2].height = 24


def write_data_rows(ws, data):
    for row_idx, row in enumerate(data, 3):
        q_type = row[1]
        if q_type == '多选题':
            bg = ACCENT_FILL
        elif q_type == '填空题':
            bg = BLANK_FILL
        else:
            bg = ROW_ODD if (row_idx % 2 == 1) else ROW_EVEN

        fill = PatternFill('solid', fgColor=bg)

        for col_idx, val in enumerate(row, 1):
            cell = ws.cell(row=row_idx, column=col_idx, value=str(val))
            cell.font = Font(name='微软雅黑', size=10)
            cell.fill = fill
            cell.border = thin_border()
            cell.alignment = Alignment(
                horizontal='left' if col_idx >= 3 else 'center',
                vertical='top',
                wrap_text=True
            )

        ws.cell(row=row_idx, column=1).alignment = Alignment(horizontal='center', vertical='center')
        ws.cell(row=row_idx, column=2).alignment = Alignment(horizontal='center', vertical='center')

