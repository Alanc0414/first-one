def set_col_widths(ws):
    widths = [6, 10, 38, 36, 10, 30, 38]
    for i, w in enumerate(widths, 1):
        ws.column_dimensions[get_column_letter(i)].width = w


def create_excel(filename, title, title_bg, data):
    wb = Workbook()
